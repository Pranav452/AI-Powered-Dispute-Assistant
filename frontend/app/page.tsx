// This function fetches the data from our FastAPI backend
async function getDisputes(): Promise<Dispute[]> {
  try {
    // The `fetch` call is made to the backend API running on port 8000
    // { cache: 'no-store' } ensures we always get the latest data
    const res = await fetch('http://127.0.0.1:8000/api/disputes', { cache: 'no-store' });

    if (!res.ok) {
      throw new Error('Failed to fetch data from backend');
    }

    return res.json();
  } catch (error) {
    console.error("API Fetch Error:", error);
    return []; // Return an empty array on error
  }
}


